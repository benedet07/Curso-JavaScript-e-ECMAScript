console.log('Vai começar...')
var c = 1   //Condição Início
while (c<=6){
    console.log(`Passo ${c}`)
    c++
}


var c = 1   //Condição Final
do{
    console.log(`Passo ${c}`)
    c++
}while(c<=6)


for(var c=1;c<=6;c++) {    //Condição de controle, bom pra usar quando sabe o limite das repetições
    console.log(`Passo ${c}`)
}
console.log('FIM!')